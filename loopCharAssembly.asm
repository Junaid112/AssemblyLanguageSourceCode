.MODEL SMALL
.STACK 100H
.DATA
PROMPT 		DB'Enter a line of text: ' ,0DH,0AH,'$'
NOCAP_MSG 	DB 'nO Capitals $'
CAP_MSG 	DB 0DH,0AH,'first Capital= '
FIRST 		DB ']'
		DB '  last Capital= '
LAST 		DB '@ $'


	




.CODE
MAIN PROC
        

	 MOV AX,@DATA
        MOV DS,AX       ; giving address ds
	



	MOV AH,9H
	LEA DX,PROMPT	;SHOW PROMPT	
	INT 21H

	
	MOV AH,1H	;INPUT CHAR
	INT 21H

WHILE_: ;NOT A CARIAGE RETURN
	CMP AL,0DH	;CR?
	JE END_WHILE	;JUMP IF YES
;IF A CAPITAL
	CMP AL,'A'	;CHAR >='A'?
	JNGE END_IF	;JUMP IF YES	
	CMP AL,'Z'	;CHAR <='Z'?
	JNLE END_IF	;JUMP IF YES	
;THEN
;IF CHAR PROCEED FIRST CAPITAL
	CMP AL,FIRST ;CHAR<FIRST
	JNL CHECK_LAST ;NO >=
;THEN FIRST = CHAR
	MOV FIRST,AL
;END IF
CHECK_LAST:
	CMP AL,LAST ;CHAR>FIRST
	JNG END_IF  ;NO <=

;THEN LAST =CHAR
	MOV LAST,AL
	
END_IF:
	INT 21H		;CHAR IN AL	
	JMP WHILE_



END_WHILE:

	MOV AH,9H	;DISPLAY STRING
; IF FIRST NOT CHANGE
	CMP FIRST,']' ;FIRST =']'
	JNE CAPS  ;DISPLAY RESULT

;TTHEN
	LEA DX,NOCAP_MSG
	JMP DISPLAY

CAPS:
	LEA DX,CAP_MSG
DISPLAY:
	INT 21H
;END


	MOV AH,4CH
	INT 21H


MAIN ENDP
END MAIN